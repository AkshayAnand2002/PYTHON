#GIVEN 2 STRINGS S1 AND S2 FIND IF THE 1ST STRING IS A SUBSEQUENCE OF SECOND.
#A SUBSEQUENCE IS A SEQUENCE THAT CAN BE DERIVED FROM ANOTHER SEQUENCE BY DELETING 
#SOME ELEMENTS WITHOUT CHANGING THE ORDER OF REMAINING ELEMENTS.
#INPUT: STR1="AXY" , STR2="ADXCPY"
#OUTPUT: TRUE
s1=input()
s2=input()
s3=''
for i in s2:
    if i in s1:
        s3+=i
# print(s3)
if s3==s1:
    print('True')
else:
    print('False')
